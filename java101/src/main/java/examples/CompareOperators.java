package examples;

public class CompareOperators {

    public static void main(String[] args) {
        int numFirst = 10;
        int numSecond = 20;

        System.out.println("numFirst " + numFirst);
        System.out.println("numSecond " + numSecond);
        System.out.println("-----------------------");

        System.out.println("numFirst = numSecond");
        System.out.println(numFirst == numSecond);
        System.out.println("numFirst < numSecond");
        System.out.println(numFirst < numSecond);
        System.out.println("numFirst > numSecond");
        System.out.println(numFirst > numSecond);
        System.out.println("numFirst <= numSecond");
        System.out.println(numFirst <= numSecond);
        System.out.println("numFirst => numSecond");
        System.out.println(numFirst >= numSecond);
        System.out.println("numFirst != numSecond");
        System.out.println(numFirst != numSecond);
    }

}
