package examples;


public class ValueAssignToVariable {

    public static int a = 5;
    public static int b;
    public static String javaWorld = "JavaWorld";


    public static void main(String[] args) {
        b = 7;

        System.out.println(a);
        System.out.println(b);
        System.out.println(javaWorld);
    }
}
